package ru.arkuzmin.diplom.optimization.fx;

public class RegressionDTO {
	
	private double x;
	private double y;
	
	
	public RegressionDTO(double x, double y) {
		this.x = x;
		this.y = y;
	}


	public double getX() {
		return x;
	}


	public void setX(double x) {
		this.x = x;
	}


	public double getY() {
		return y;
	}


	public void setY(double y) {
		this.y = y;
	}
	
	
}
